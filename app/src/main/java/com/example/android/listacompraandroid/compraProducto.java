/**
 * Clase que define la compra de un producto
 *
 * @author Ramiro Vargas, Veronica Hevia
 */

public class compraProducto {
    String nombre;
    String precio;
    int foto;

    compraProducto(String nombre, String precio, int foto) {
        this.nombre = nombre;
        this.precio = precio;
        this.foto = foto;
    }

}
